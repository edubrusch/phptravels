package br.com.eduardo.phptravels.invoice;

public class DTOInvoice {
	
	private String email;
	private String password;
	private String hotelName;
	private String invoiceDate;
	private String dueDate;
	private String invoiceNumber;
	private String bookingCode;
	private String customerDetails;
	private String room;
	private String checkIn;
	private String checkOut;
	private String totalStay;
	private String depositNow;
	private String taxVAT;
	private String totalAmount;
	
	
	
	public String getEmail() {
		return email;
	}
	public void setEmail(String email) {
		this.email = email;
	}
	public String getPassword() {
		return password;
	}
	public void setPassword(String password) {
		this.password = password;
	}
	public String getHotelName() {
		return hotelName;
	}
	public void setHotelName(String hotelName) {
		this.hotelName = hotelName;
	}
	public String getInvoiceDate() {
		return invoiceDate;
	}
	public void setInvoiceDate(String invoiceDate) {
		this.invoiceDate = invoiceDate;
	}
	public String getDueDate() {
		return dueDate;
	}
	public void setDueDate(String dueDate) {
		this.dueDate = dueDate;
	}
	public String getInvoiceNumber() {
		return invoiceNumber;
	}
	public void setInvoiceNumber(String invoiceNumber) {
		this.invoiceNumber = invoiceNumber;
	}
	public String getBookingCode() {
		return bookingCode;
	}
	public void setBookingCode(String bookingCode) {
		this.bookingCode = bookingCode;
	}
	public String getCustomerDetails() {
		return customerDetails;
	}
	public void setCustomerDetails(String customerDetails) {
		this.customerDetails = customerDetails;
	}
	public String getRoom() {
		return room;
	}
	public void setRoom(String room) {
		this.room = room;
	}
	public String getCheckIn() {
		return checkIn;
	}
	public void setCheckIn(String checkIn) {
		this.checkIn = checkIn;
	}
	public String getCheckOut() {
		return checkOut;
	}
	public void setCheckOut(String checkOut) {
		this.checkOut = checkOut;
	}
	public String getTotalStay() {
		return totalStay;
	}
	public void setTotalStay(String totalStay) {
		this.totalStay = totalStay;
	}
	public String getDepositNow() {
		return depositNow;
	}
	public void setDepositNow(String depositNow) {
		this.depositNow = depositNow;
	}
	public String getTaxVAT() {
		return taxVAT;
	}
	public void setTaxVAT(String taxVAT) {
		this.taxVAT = taxVAT;
	}
	public String getTotalAmount() {
		return totalAmount;
	}
	public void setTotalAmount(String totalAmount) {
		this.totalAmount = totalAmount;
	}

}
